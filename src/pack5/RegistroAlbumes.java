    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigoBuscar = codigo.getText();
        boolean encontrado = false;
        if (codigoBuscar.isEmpty()) {
            //si el campo esta vacio, muestra error
            JOptionPane.showMessageDialog(null, "Por favor, ingrese el codigo.");
            return;
        }
        for (int i = 0; i < albumindex; i++) {
            if (Albumes[i][4].equals(codigoBuscar)) {
                //muestra los datos del album con dicho codigo
                grupo.setText(Albumes[i][0]);
                nombre.setText(Albumes[i][1]);
                año.setText(Albumes[i][2]);
                favorito.setText(Albumes[i][3]);
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "Codigo no encontrado.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

