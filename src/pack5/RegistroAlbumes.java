    private void btnElimiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElimiarActionPerformed
        // TODO add your handling code here:
        String codigoEliminar = codigo.getText();
        boolean encontrado = false;

        if (codigoEliminar.isEmpty()) {
            //solicita el codigo y si no se ingresa nada, muestra error
            JOptionPane.showMessageDialog(null, "Por favor, ingrese el codigo.");
            return;
        }
        for (int i = 0; i < albumindex; i++) {
            //verifica si el codigo coincide con algun dato guardado
            if (Albumes[i][4].equals(codigoEliminar)) {
                //elimina el album con dicho codigo
                for (int j = i; j < albumindex - 1; j++) {
                    Albumes[j] = Albumes[j + 1];
                }
                albumindex--;
                encontrado = true;
                JOptionPane.showMessageDialog(null, "Album eliminado.");
                break; //sale del bucle
            }
        }

        //si no encuntra ninguna coincidencia con el codigo muestra error
        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "Codigo no encontrado.");
        }

        //se limpian los campos de texto
        grupo.setText("");
        nombre.setText("");
        año.setText("");
        favorito.setText("");
        codigo.setText("");
    }//GEN-LAST:event_btnElimiarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String campo1 = grupo.getText();
        String campo2 = nombre.getText();
        String campo3 = año.getText();
        String campo4 = favorito.getText();
        String campo5 = codigo.getText();
        
        //verifica si los campos estan vacios o no
        if (campo1.isEmpty() || campo2.isEmpty() || campo3.isEmpty() || campo4.isEmpty() || campo5.isEmpty()) {
            //si alguno esta vacio mestra un msj
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
            return;
        }
        //los datos se guardan dentro de una matriz (esta en el public)
        Albumes[albumindex][0] = campo1;
        Albumes[albumindex][1] = campo2;
        Albumes[albumindex][2] = campo3;
        Albumes[albumindex][3] = campo4;
        Albumes[albumindex][4] = campo5;

        albumindex++;
        //luego de guardar se limpia el registro
        grupo.setText("");
        nombre.setText("");
        año.setText("");
        favorito.setText("");
        codigo.setText("");
        
        JOptionPane.showMessageDialog(null, "Se guardo correctamente");
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        StringBuilder datosAlbum = new StringBuilder();
        for(int i = 0; i < albumindex; i++){
            //agregamos los datos de nuestro album
            datosAlbum.append("\tRegistro de Albumes\n")
            .append("Grupo: ").append(Albumes[i][0]).append("\n")
            .append("Nombre: ").append(Albumes[i][1]).append("\n")
            .append("Año: ").append(Albumes[i][2]).append("\n")
            .append("Favorito: ").append(Albumes[i][3]).append("\n")
            .append("Código: ").append(Albumes[i][4]).append("\n\n");
        }

        //mostramos nuestros datos guardados
        pMostrar.setText(datosAlbum.toString());
    }                                          

